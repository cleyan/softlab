//Insert Method @2-1EBE3E92
    function Insert()
    {
        global $CCSLocales;
        global $DefaultDateFormat;
        $this->CmdExecution = true;
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeBuildInsert", $this->Parent);
        $this->InsertFields["nom_metodo"]["Value"] = $this->nom_metodo->GetDBValue(true);
        $this->InsertFields["imprimir"]["Value"] = $this->imprimir->GetDBValue(true);
        $this->SQL = CCBuildInsert("metodos", $this->InsertFields, $this);
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeExecuteInsert", $this->Parent);
        if($this->Errors->Count() == 0 && $this->CmdExecution) {
            $this->query($this->SQL);
            $this->CCSEventResult = CCGetEvent($this->CCSEvents, "AfterExecuteInsert", $this->Parent);
        }
    }
//End Insert Method

//Update Method @2-C43BB5BC
    function Update()
    {
        global $CCSLocales;
        global $DefaultDateFormat;
        $this->CmdExecution = true;
        $Where = "";
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeBuildUpdate", $this->Parent);
        $this->UpdateFields["nom_metodo"]["Value"] = $this->nom_metodo->GetDBValue(true);
        $this->UpdateFields["imprimir"]["Value"] = $this->imprimir->GetDBValue(true);
