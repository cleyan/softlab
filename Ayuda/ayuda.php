        $this->ErrorBlock = "Grid ayuda";
        $this->Initialize();
        $this->titulo = new clsField("titulo", ccsText, "");
        
        $this->contenido = new clsField("contenido", ccsMemo, "");
        

    }
//End DataSourceClass_Initialize Event

//SetOrder Method @5-9E1383D1
    function SetOrder($SorterName, $SorterDirection)
    {
        $this->Order = "";
        $this->Order = CCGetOrder($this->Order, $SorterName, $SorterDirection, 
            "");
    }
//End SetOrder Method

//Prepare Method @5-BD9D10FF
    function Prepare()
    {
        global $CCSLocales;
        global $DefaultDateFormat;
        $this->wp = new clsSQLParameters($this->ErrorBlock);
        $this->wp->AddParameter("1", "urlpagina", ccsText, "", "", $this->Parameters["urlpagina"], "", false);
        $this->wp->AddParameter("2", "urlayuda_id", ccsInteger, "", "", $this->Parameters["urlayuda_id"], "", false);
        $this->wp->Criterion[1] = $this->wp->Operation(opEqual, "pagina", $this->wp->GetDBValue("1"), $this->ToSQL($this->wp->GetDBValue("1"), ccsText),false);
        $this->wp->Criterion[2] = $this->wp->Operation(opEqual, "ayuda_id", $this->wp->GetDBValue("2"), $this->ToSQL($this->wp->GetDBValue("2"), ccsInteger),false);
        $this->Where = $this->wp->opAND(
             false, 
             $this->wp->Criterion[1], 
             $this->wp->Criterion[2]);
    }
//End Prepare Method

//Open Method @5-CA2DD41A
    function Open()
    {
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeBuildSelect", $this->Parent);
        $this->CountSQL = "SELECT COUNT(*)\n\n" .
        "FROM ayuda";
        $this->SQL = "SELECT * \n\n" .
        "FROM ayuda {SQL_Where} {SQL_OrderBy}";
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeExecuteSelect", $this->Parent);
        if ($this->CountSQL) 
            $this->RecordsCount = CCGetDBValue(CCBuildSQL($this->CountSQL, $this->Where, ""), $this);
        else
            $this->RecordsCount = "CCS not counted";
        $this->query($this->OptimizeSQL(CCBuildSQL($this->SQL, $this->Where, $this->Order)));
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "AfterExecuteSelect", $this->Parent);
    }
//End Open Method

//SetValues Method @5-E844713F
    function SetValues()
    {
        $this->titulo->SetDBValue($this->f("titulo"));
        $this->contenido->SetDBValue($this->f("contenido"));
    }
//End SetValues Method

} //End ayudaDataSource Class @5-FCB6E20C

