        $this->SorterName = CCGetParam("gruposOrder", "");
        $this->SorterDirection = CCGetParam("gruposDir", "");

        $this->grupo_id = new clsControl(ccsLabel, "grupo_id", "grupo_id", ccsInteger, "", CCGetRequestParam("grupo_id", ccsGet, NULL), $this);
        $this->nom_grupo = new clsControl(ccsLabel, "nom_grupo", "nom_grupo", ccsText, "", CCGetRequestParam("nom_grupo", ccsGet, NULL), $this);
        $this->titulo = new clsControl(ccsLabel, "titulo", "titulo", ccsText, "", CCGetRequestParam("titulo", ccsGet, NULL), $this);
